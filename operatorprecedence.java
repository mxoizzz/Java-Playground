public class operatorprecedence {
    public static void main(String[] args) {
        int ans = 2 + 3 -1;
        System.out.println(ans);
        
        ans = 5 + (4 -1);
        System.out.println(ans);
        
        ans = 4 + 5 * 3;
        System.out.println(ans);
        
        ans = 64 / 4 * 1;
        System.out.println(ans);



    }
}
